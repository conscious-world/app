import UIKit

extension UIColor {
    
    static func originalColor() -> UIColor{
        return UIColor(hexString: "#FAC54B")
    }
    
    static func rageColor() -> UIColor{
        return UIColor(hexString: "#f96c6c")
    }
    
    static func fearColor() -> UIColor{
        return UIColor(hexString: "#b2f96c")
    }
    
    static func amazementColor() -> UIColor{
        return UIColor(hexString: "#6cf9f9")
    }
    
    static func loathingColor() -> UIColor{
        return UIColor(hexString: "#b26cf9")
    }
    
    static func joyColor() -> UIColor{
        return UIColor(hexString: "#f4eb24")
    }

}
